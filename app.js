// Generate Tabs and Pages on load
document.addEventListener('DOMContentLoaded', () => {
  const tabsContainer = document.getElementById('tabs-container');
  const pagesWrapper = document.getElementById('pages-wrapper');

  recipes.forEach((recipe, index) => {
    // 1. Create Tab
    const tab = document.createElement('div');
    tab.className = 'tab';
    tab.style.backgroundColor = recipe.color;
    tab.innerText = recipe.state;
    tab.onclick = () => openPage(recipe.id);
    tabsContainer.appendChild(tab);

    // 2. Create Page
    const page = document.createElement('div');
    page.id = recipe.id;
    page.className = 'page';
    
    let ingredientsHTML = '';
    recipe.ingredients.forEach(ing => {
      ingredientsHTML += `<li>${ing}</li>`;
    });

    let stepsHTML = '';
    recipe.steps.forEach(step => {
      stepsHTML += `<li>${step}</li>`;
    });

    page.innerHTML = `
      <div class="recipe-header">
        <div class="recipe-title-wrapper">
          <span class="recipe-state">${recipe.state}</span>
          <h1 class="recipe-title">${recipe.name}</h1>
        </div>
        <div class="recipe-image-container">
          <div class="tape"></div>
          <img src="${recipe.image}" alt="${recipe.name}" class="recipe-image">
        </div>
      </div>

      <div class="uniqueness-box">
        ${recipe.uniqueness}
      </div>

      <h2 class="section-title"><i class="fa-solid fa-basket-shopping"></i> सामग्री (Ingredients)</h2>
      <ul class="ingredients-list">
        ${ingredientsHTML}
      </ul>

      <h2 class="section-title"><i class="fa-solid fa-list-ul"></i> बनाने की विधि (Steps)</h2>
      <ol class="steps-list">
        ${stepsHTML}
      </ol>
    `;
    
    pagesWrapper.appendChild(page);
  });
});

function openPage(pageId) {
  // Hide all pages
  document.querySelectorAll('.page').forEach(page => {
    page.classList.remove('active');
  });
  
  // Show target page
  document.getElementById(pageId).classList.add('active');
}
